/**
 * Hub/Spoke Color Name Data Structure
 *
 * Each hue hub contains semantically-appropriate color names organized by
 * lightness and chroma bands. The hub (hue) is the semantic anchor - it
 * determines what kind of color this is. The spokes (L×C matrix) contain
 * words that are semantically compatible with both the hue AND the L/C values.
 *
 * Structure:
 * - 18 hue hubs (20° each)
 * - Each hub has a 5×4 matrix (L bands × C bands)
 * - Each cell contains 10+ semantically-appropriate words
 * - Temperature selects which hub variant to use
 * - Fractional L/C values sub-select within the word list
 */

/**
 * Lightness bands (5 levels)
 * Maps to L bucket ranges for semantic compatibility
 */
export type LightnessBand = 'veryDark' | 'dark' | 'mid' | 'light' | 'veryLight';

/**
 * Chroma bands (4 levels)
 * Maps to C bucket ranges for semantic compatibility
 */
export type ChromaBand = 'muted' | 'moderate' | 'saturated' | 'vivid';

/**
 * A cell in the L×C matrix containing semantically-appropriate words
 */
export type HueCell = readonly string[];

/**
 * The L×C matrix for a single hue hub
 * Organized as [LightnessBand][ChromaBand] → words[]
 */
export type HueMatrix = {
  readonly [L in LightnessBand]: {
    readonly [C in ChromaBand]: HueCell;
  };
};

/**
 * A complete hue hub with temperature variants
 */
export type HueHub = {
  readonly warm: HueMatrix;
  readonly cool: HueMatrix;
  readonly neutral: HueMatrix;
};

/**
 * Map L bucket (0-9) to lightness band
 */
export function getLightnessBand(lBucket: number): LightnessBand {
  if (lBucket <= 1) return 'veryDark'; // 0.0-0.2
  if (lBucket <= 3) return 'dark'; // 0.2-0.4
  if (lBucket <= 5) return 'mid'; // 0.4-0.6
  if (lBucket <= 7) return 'light'; // 0.6-0.8
  return 'veryLight'; // 0.8-1.0
}

/**
 * Map C bucket (0-7) to chroma band
 */
export function getChromaBand(cBucket: number): ChromaBand {
  if (cBucket <= 1) return 'muted'; // 0.00-0.06
  if (cBucket <= 3) return 'moderate'; // 0.06-0.15
  if (cBucket <= 5) return 'saturated'; // 0.15-0.25
  return 'vivid'; // 0.25+
}

/**
 * Calculate sub-index within a word list using fractional position
 * This uses the "leftover" precision after bucketing to deterministically
 * select which synonym to use.
 *
 * @param value - The original L, C, or H value
 * @param bucketSize - The size of each bucket
 * @param wordCount - Number of words available in the cell
 * @returns Index into the word array (0 to wordCount-1)
 */
export function getSubIndex(value: number, bucketSize: number, wordCount: number): number {
  if (wordCount <= 1) return 0;
  // Get position within bucket (0.0 to 1.0)
  const withinBucket = (value % bucketSize) / bucketSize;
  // Map to word index
  return Math.min(Math.floor(withinBucket * wordCount), wordCount - 1);
}

