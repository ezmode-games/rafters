/**
 * Check if a hue bucket has an expanded hub available
 */
export function hasExpandedHub(hBucket: number): boolean {
  return HUE_HUBS[hBucket] !== undefined;
}

/**
 * Get material word from expanded hub if available
 * Falls back to undefined if hub doesn't exist
 */
export function getExpandedMaterialWord(
  hBucket: number,
  lBucket: number,
  cBucket: number,
  temperature: 'warm' | 'cool' | 'neutral',
  l: number,
  _c: number, // Reserved for future sub-selection based on chroma fraction
): string | undefined {
  const hub = HUE_HUBS[hBucket];
  if (!hub) return undefined;

  const lBand = getLightnessBand(lBucket);
  const cBand = getChromaBand(cBucket);

  const matrix = hub[temperature];
  const cell = matrix[lBand][cBand];

  // Use fractional L value to sub-select within the word list
  // L bucket size is 0.1
  const subIndex = getSubIndex(l, 0.1, cell.length);

  return cell[subIndex];
}
